import {
	existsSync,
	mkdirSync,
	readFileSync,
	readdirSync,
	statSync,
	writeFileSync,
} from "node:fs";
import { dirname, join, relative, sep } from "node:path";
import { fileURLToPath } from "node:url";

const GENERATED_MARKER = "Generated by generate-codex-mirror.mjs";
const scriptDir = dirname(fileURLToPath(import.meta.url));
const packageRoot = dirname(scriptDir);
const repoRoot = dirname(dirname(packageRoot));
const referenceRoot = join(repoRoot, ".reference/codex/codex-rs");
const mirrorRoot = join(packageRoot, "src/upstream/codex-rs");
const ledgerPath = join(packageRoot, "CODEX_PARITY_LEDGER.json");

if (!existsSync(referenceRoot)) {
	throw new Error(`Reference Codex source is missing: ${referenceRoot}`);
}

mkdirSync(mirrorRoot, { recursive: true });

const referenceRustFiles = walk(referenceRoot).filter((path) => path.endsWith(".rs"));
const referenceCrates = topLevelCrates(referenceRoot);
for (const crate of referenceCrates) {
	mkdirSync(join(mirrorRoot, crate), { recursive: true });
}

const files = [];
for (const referenceFile of referenceRustFiles) {
	const referencePath = slash(relative(referenceRoot, referenceFile));
	const mirrorPath = mirrorPathForReference(referencePath);
	const absoluteMirrorPath = join(mirrorRoot, mirrorPath);
	const crate = referencePath.split("/")[0] ?? "";

	if (!existsSync(absoluteMirrorPath)) {
		mkdirSync(dirname(absoluteMirrorPath), { recursive: true });
		writeFileSync(
			absoluteMirrorPath,
			stubSource({
				crate,
				referencePath,
				mirrorPath,
				reason: reasonForPath(referencePath),
			}),
		);
	}

	files.push({
		crate,
		referencePath,
		mirrorPath,
		reason: reasonForPath(referencePath),
		status: statusForMirrorFile(absoluteMirrorPath),
	});
}

files.sort((a, b) => a.referencePath.localeCompare(b.referencePath));
const totals = {
	referenceCrates: referenceCrates.length,
	referenceRustFiles: referenceRustFiles.length,
	implemented: files.filter((file) => file.status === "implemented").length,
	platform_adaptation: files.filter((file) => file.status === "platform_adaptation")
		.length,
	stubbed: files.filter((file) => file.status === "stubbed").length,
	missing: files.filter((file) => file.status === "missing").length,
};

writeFileSync(
	ledgerPath,
	`${JSON.stringify(
		{
			schema: "codex-rs-typescript-parity-ledger.v1",
			referenceRoot: ".reference/codex/codex-rs",
			mirrorRoot: "packages/codex-js/src/upstream/codex-rs",
			totals,
			crates: referenceCrates,
			files,
		},
		null,
		2,
	)}\n`,
);

function walk(root) {
	const entries = readdirSync(root).flatMap((entry) => {
		const path = join(root, entry);
		const stat = statSync(path);
		if (stat.isDirectory()) {
			return walk(path);
		}
		return [path];
	});
	return entries.sort();
}

function topLevelCrates(root) {
	return readdirSync(root)
		.filter((entry) => {
			const path = join(root, entry);
			if (!statSync(path).isDirectory() || entry.startsWith(".")) {
				return false;
			}
			return existsSync(join(path, "Cargo.toml")) || containsRustSource(path);
		})
		.sort();
}

function containsRustSource(path) {
	return walk(path).some((file) => file.endsWith(".rs"));
}

function mirrorPathForReference(referencePath) {
	const parts = referencePath.split("/");
	const file = parts.pop();
	if (!file) {
		throw new Error(`Invalid reference path: ${referencePath}`);
	}
	const stem = file.slice(0, -".rs".length);
	const targetFile = `${stem}.ts`;
	return [...parts, targetFile].join("/");
}

function statusForMirrorFile(path) {
	if (!existsSync(path)) {
		return "missing";
	}
	const source = readFileSync(path, "utf8");
	if (source.includes(GENERATED_MARKER)) {
		return "stubbed";
	}
	if (source.includes("@codex-platform-adaptation")) {
		return "platform_adaptation";
	}
	return "implemented";
}

function stubSource(input) {
	const unsupportedPath = relativeImport(dirname(join(mirrorRoot, input.mirrorPath)), join(mirrorRoot, "unsupported"));
	return `// ${GENERATED_MARKER}\n// Reference: ${input.referencePath}\n\nimport { unsupportedCodexFeature } from "${unsupportedPath}";\n\nexport const codexReference = {\n\tcrate: ${JSON.stringify(input.crate)},\n\treferencePath: ${JSON.stringify(input.referencePath)},\n\treason: ${JSON.stringify(input.reason)},\n\tstatus: "stubbed",\n} as const;\n\nexport function unsupported(..._args: unknown[]): never {\n\treturn unsupportedCodexFeature(codexReference);\n}\n\nexport default unsupported;\n`;
}

function reasonForPath(referencePath) {
	if (referencePath.includes("/tui/") || referencePath.startsWith("tui/")) {
		return "TUI only";
	}
	if (referencePath.includes("/cli/") || referencePath.startsWith("cli/")) {
		return "CLI only";
	}
	if (
		referencePath.includes("sandbox") ||
		referencePath.includes("landlock") ||
		referencePath.includes("bwrap") ||
		referencePath.includes("windows")
	) {
		return "native sandbox";
	}
	if (
		referencePath.includes("pty") ||
		referencePath.includes("exec") ||
		referencePath.includes("process")
	) {
		return "native process";
	}
	return "not ported yet";
}

function relativeImport(fromDirectory, toFileWithoutExtension) {
	let value = slash(relative(fromDirectory, toFileWithoutExtension));
	if (!value.startsWith(".")) {
		value = `./${value}`;
	}
	return value;
}

function slash(path) {
	return path.split(sep).join("/");
}
